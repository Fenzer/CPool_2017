int     ft_atoi(char *a)
{
    int r,s;

    s = 1;
    r = 0; 
    while (*a <= ' ')
        a++;
    if (*a == '-' || *a == '+')
    {
        if (*a == '-')
            s = -1;
        a++;
        if (!(*a >= '0' && *a <= '9'))
            return (r);
    }
    while (*a >= '0' && *a <= '9')
    {
        r *= 10;
        r += *a++ - '0';
    }
    return (s * r);
}
